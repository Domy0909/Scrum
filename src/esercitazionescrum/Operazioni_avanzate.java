public class Operazioni_avanzate {
    
     public Operazioni_avanzate() {
    }
    public double logaritmo_basedieci(double a){
        return Math.log10(a);
    }
    public double logaritmo_naturale(double a){
        return Math.log(a);
    }
    public double radice_quadrata(double a){
        return Math.sqrt(a);
    }
    public double radice(double a, double b){
        return Math.pow(a, 1/b);
    }
    public double potenza(double a, double b){
        return Math.pow(a, b);
    }
    
}
